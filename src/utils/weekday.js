export function weekday(weekday) {
    switch (weekday) {
        case 'Mon':
            return day = {
                name: 'Monday'
            };
            break;
        case 'Tue':
            return day = {
                name: 'Tuesday'
            };
            break;
        case 'Wed':
            return day = {
                name: 'Wednesday'
            };
            break;
        case 'Thu':
            return day = {
                name: 'Thursday'
            };
            break;
        case 'Fri':
            return day = {
                name: 'Friday'
            };
            break;
        case 'Sat':
            return day = {
                name: 'Saturday'
            };
            break;
        case 'Sun':
            return day = {
                name: 'Sunday'
            };
            break;
        
    }
}